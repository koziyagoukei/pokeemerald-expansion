static bool32 FrontierCreator_CanGmax(enum Species species)
{
    u32 i;

    for (i = 0; sFrontierCreatorGmaxCapableSpecies[i] != SPECIES_NONE; i++)
    {
        if (sFrontierCreatorGmaxCapableSpecies[i] == species)
            return TRUE;
    }

    return FALSE;
}

static bool32 FrontierCreator_IsAllowedBaseSpecies(enum Species species)
{
    u32 i;

    for (i = 0; sFrontierCreatorAllowedBaseSpecies[i] != SPECIES_NONE; i++)
    {
        if (sFrontierCreatorAllowedBaseSpecies[i] == species)
            return TRUE;
    }

    return FALSE;
}

static bool32 FrontierCreator_IsBannedSpecies(enum Species species)
{
    if (species == SPECIES_NONE || species >= NUM_SPECIES)
        return TRUE;

#ifdef SPECIES_CUSTOM_START
    if (species >= SPECIES_CUSTOM_START)
        return TRUE;
#endif

    if (species == SPECIES_PIKACHU_STARTER || species == SPECIES_EEVEE_STARTER)
        return TRUE;

    if (!FrontierCreator_IsAllowedBaseSpecies(species))
        return TRUE;

    if (!IsSpeciesEnabled(species))
        return TRUE;

    return FALSE;
}

